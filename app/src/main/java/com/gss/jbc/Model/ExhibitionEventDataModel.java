package com.gss.jbc.Model;

import java.util.Calendar;

public class ExhibitionEventDataModel {

    String EventDate, EventTitle, EventVenue, EventLogo, EventLink;

    public String getEventDate() { return EventDate; }
    public void setEventDate(String day) { EventDate = day; }

    public String getEventTitle() { return EventTitle; }
    public void setEventTitle(String day) { EventTitle = day; }

    public String getEventVenue() { return EventVenue; }
    public void setEventVenue(String day) { EventVenue = day; }

    public String getEventLogo() { return EventLogo; }
    public void setEventLogo(String day) { EventLogo = day; }

    public String getEventLink() { return EventLink; }
    public void setEventLink(String day) { EventLink = day; }
}
